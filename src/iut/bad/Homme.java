package iut.bad;

public class Homme {
	private String nom;
	private String prenom;
	private int age;
	
	public Homme() {
		super();
	}
	
	public Homme(String nom, String prenom) {
		super();
		this.nom = nom;
		this.prenom = prenom;
	}
	
	public Homme(String nom, String prenom, int age) {
		super();
		this.nom = nom;
		this.prenom = prenom;
		this.age = age;
	}
	
	
}
